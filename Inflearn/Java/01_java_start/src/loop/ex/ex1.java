package loop.ex;

public class ex1 {
    public static void main(String[] args) {
        for (int count = 1; count <= 10; count++) {
            System.out.println(count);
        }
        System.out.println();

        int count = 1;
        while (count <=10){
            System.out.println(count);
            count++;
        }
    }
}
